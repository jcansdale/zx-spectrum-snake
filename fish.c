#define ATTR_BASE ((unsigned char *)0x5800)

void setAttr(unsigned char x, unsigned char y, unsigned char attr) { ATTR_BASE[y * 32 + x] = attr; }

unsigned char* getPixelAddr(unsigned char x, unsigned char y) {
    unsigned int addr = 0x4000;
    addr |= ((y & 0xC0) << 5) | ((y & 0x07) << 8) | ((y & 0x38) << 2) | (x >> 3);
    return (unsigned char *)addr;
}

void clearScreen(unsigned char attr) {
    memset((void *)0x4000, 0, 6144);
    memset((void *)0x5800, attr, 768);
}

