void drawFish(void) {
    unsigned int block_idx = 0;
    for (unsigned char block_y = 0; block_y < 24; block_y++) {
        for (unsigned char block_x = 0; block_x < 32; block_x++) {
            setAttr(block_x, block_y, attrData[block_y * 32 + block_x]);
            for (unsigned char row = 0; row < 8; row++) {
                *getPixelAddr(block_x * 8, block_y * 8 + row) = pixelData[block_idx * 8 + row];
            }
            block_idx++;
        }
    }
}

unsigned char bubbleY[4] = {150, 120, 90, 60};
unsigned char bubbleX[4] = {216, 224, 212, 230};

void plotPixel(unsigned char x, unsigned char y) {
    if (y >= 192 || x >= 256) return;
    unsigned char *addr = getPixelAddr(x, y);
    *addr |= (0x80 >> (x & 7));
}

void clearPixel(unsigned char x, unsigned char y) {
    if (y >= 192 || x >= 256) return;
    unsigned char *addr = getPixelAddr(x, y);
    *addr &= ~(0x80 >> (x & 7));
}

void drawBubble(unsigned char x, unsigned char y) {
    if (y < 12 || y > 180) return;
    // Draw a small circle
    plotPixel(x, y-2);
    plotPixel(x-1, y-1);
    plotPixel(x+1, y-1);
    plotPixel(x-2, y);
    plotPixel(x+2, y);
    plotPixel(x-1, y+1);
    plotPixel(x+1, y+1);
    plotPixel(x, y+2);
}

void clearBubble(unsigned char x, unsigned char y) {
    if (y < 12 || y > 180) return;
    clearPixel(x, y-2);
    clearPixel(x-1, y-1);
    clearPixel(x+1, y-1);
    clearPixel(x-2, y);
    clearPixel(x+2, y);
    clearPixel(x-1, y+1);
    clearPixel(x+1, y+1);
    clearPixel(x, y+2);
}

void main(void) {
    unsigned char i, frame = 0;
    unsigned char j;
    
    clearScreen(INK_BLUE | PAPER_CYAN);
    drawFish();
    zx_border(INK_BLUE);
    
    // Set bubble area attributes to white ink on cyan paper for visibility
    for (i = 26; i < 31; i++) {
        for (j = 0; j < 24; j++) {
            // Only change if currently just cyan
            if (ATTR_BASE[j * 32 + i] == (INK_BLUE | PAPER_CYAN | BRIGHT)) {
                ATTR_BASE[j * 32 + i] = INK_WHITE | PAPER_CYAN | BRIGHT;
            }
        }
    }
    
    // Small delay to let tape loading finish
    for (i = 0; i < 50; i++) {
        intrinsic_halt();
    }
    
    // Animation loop - runs forever
    while (1) {
        for (i = 0; i < 4; i++) {
            clearBubble(bubbleX[i], bubbleY[i]);
            bubbleY[i]--;
            if (bubbleY[i] < 15) {
                bubbleY[i] = 165 + (i * 6);
            }
            // Wobble
            if ((frame & 31) == 0) {
                bubbleX[i] += (i & 1) ? 1 : -1;
            } else if ((frame & 31) == 16) {
                bubbleX[i] += (i & 1) ? -1 : 1;
            }
            // Keep in bounds
            if (bubbleX[i] < 210) bubbleX[i] = 210;
            if (bubbleX[i] > 245) bubbleX[i] = 245;
            
            drawBubble(bubbleX[i], bubbleY[i]);
        }
        
        frame++;
        
        // Wait 3 frames
        intrinsic_halt();
        intrinsic_halt();
        intrinsic_halt();
    }
}
